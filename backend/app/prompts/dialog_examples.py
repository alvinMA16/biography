# 预设对话示例
# 通过 dialog_context 注入，教模型正确的对话模式
# 这些示例会随着对话轮次增多而被丢弃，但模型已经"学会"了模式

# 示例对话：展示正确的对话模式
DIALOG_EXAMPLES = [
    # 示例 1：物 → 人（不追问物件细节，而是问背后的人）
    {"role": "user", "text": "我小时候有个铁皮青蛙，上发条就会跳"},
    {"role": "assistant", "text": "铁皮青蛙啊，那时候很流行。这个玩具是谁给您买的？"},

    # 示例 2：继续挖人，不展开物
    {"role": "user", "text": "我爸出差带回来的"},
    {"role": "assistant", "text": "您爸出差带回来的。他那时候经常出差吗？都去哪些地方？"},

    # 示例 3：事 → 情（从事件描述转向感受）
    {"role": "user", "text": "他去过很多地方，东北、上海都去过"},
    {"role": "assistant", "text": "去过这么多地方。他出差的时候，您和家里人是什么感觉？想他吗？"},

    # ===== 示例结束标记 =====
    # 明确告诉模型：上面是示例，下面才是正式对话
    {"role": "user", "text": "【以上是对话示例，展示正确的对话方式。示例中的具体内容（铁皮青蛙、出差等）与当前用户无关，请忽略这些具体信息。下面开始正式对话。】"},
    {"role": "assistant", "text": "明白了，示例中的内容只是参考，不是您的真实信息。现在开始正式对话。"},
]


def get_examples():
    """获取预设对话示例列表"""
    return DIALOG_EXAMPLES.copy()
