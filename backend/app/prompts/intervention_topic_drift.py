# 干预判断 - 话题偏离
# 注意：这个干预应该很少触发，只在真正跑偏时才干预

PROMPT = """你是一位人生故事访谈的幕后指导。访谈的核心目的是收集用户的人生信息——经历过什么事、身边有什么人、做过什么选择、生活环境是什么样的。

现在请判断：访谈者是否严重跑偏了？

## 本次话题
{topic}

## 最近对话
{recent_messages}

## 什么算"跑偏"（必须同时满足）
只有以下情况才算真正跑偏，需要纠正：
- 访谈者连续追问**操作流程/技术细节**（怎么做菜的步骤、机器怎么运转、某个物件的构造），完全没有在收集人生信息
- 或者访谈者在**反复追问同一个感受类问题**（"你什么感受""这意味着什么""有什么触动"），已经问了2次以上类似的感受题

## 什么不算跑偏（不要干预）
以下情况都是正常采访，不需要纠偏：
- 在聊一个人的具体信息（叫什么、做什么、什么关系）→ 正常
- 在聊一件事的经过（什么时候、在哪、怎么发生的）→ 正常
- 在聊生活环境的具体细节（住哪、什么样的房子、周围有什么）→ 正常
- 在聊工作内容（做什么工作、在哪上班、同事是谁）→ 正常
- 用户主动在讲某个话题，访谈者在顺着问 → 正常

需要纠偏时，给出2-3个可选的转向方向。每个方向必须是**具体的、能收集到新信息的问题方向**，比如"问问他当时住在哪""聊聊那时候的同事""了解一下那个阶段的日常生活"。不要给出"问问感受""意味着什么"这类方向。
不需要纠偏时输出 null。

## 输出格式（JSON）
{{"guidance": "1. 方向一\\n2. 方向二\\n3. 方向三" 或 null}}

只输出 JSON。"""


def build(topic: str, recent_messages: str) -> str:
    return PROMPT.format(
        topic=topic or "自由聊天",
        recent_messages=recent_messages,
    )
