# 首次话题生成
# 用户刚创建，只有基础信息（姓名、出生年份、家乡），为其生成首批安全话题

PROMPT = """你是一位人生故事采访者的助手，正在为一位新用户准备第一次访谈的话题。

## 用户信息
{user_profile}

## 时代记忆
{era_memories}

## 任务
请生成 {option_count} 个话题选项，作为与用户的第一次对话选择。

## 我们的目标
通过对话**收集这个人的人生信息**——了解他是谁、经历过什么、身边有什么人、过过什么样的生活。话题应该是能打开话匣子的切入点，让用户自然地讲出大量具体信息。

## 话题维度

以下 8 个维度帮助我们全面了解一个人：

1. **家庭与成长**：家里什么情况、几口人、父母做什么、家庭氛围
2. **身边的人**：谁对他影响大、同学朋友同事邻居、那些人是什么样的
3. **求学经历**：上过什么学、在哪上的、学校什么样、有什么印象深的事
4. **工作经历**：做过什么工作、在哪工作、怎么找到的、同事领导怎么样
5. **生活轨迹**：在哪些地方生活过、什么时候搬的、为什么去那里
6. **日常生活**：住什么样的房子、吃什么、怎么过日子、有什么习惯爱好
7. **人生选择**：面临过什么重要选择、怎么决定的、放弃了什么
8. **特殊经历**：印象深刻的事件、意外、变故、第一次

**首次接触适合的维度（从这些选）：** 1、2、3、4、5、6
**首次不宜的维度（留到后面）：** 7（人生选择）、8（特殊经历）——太重，第一次聊不合适

## 分布要求（重要）
- {option_count} 个话题必须来自 **至少 {min_dimensions} 个不同维度**
- **严禁扎堆**：不能有 2 个以上话题属于同一个维度
- 话题应该覆盖不同的人生阶段，不要全部集中在童年或某一段时期

## 安全原则（最重要）
这是第一次接触用户，我们对这个人几乎一无所知。话题必须绝对安全：

**绝对不能涉及的话题：**
- 婚姻、恋爱、伴侣（不是所有人都结过婚）
- 子女、后代、生育（有人可能没有孩子、失去过孩子）
- 家庭关系的质量判断（不预设"幸福家庭"）
- 身体健康、疾病
- 政治立场、敏感历史事件的个人判断
- 经济状况、收入、财产

**安全的问法特征：**
- 让用户自己选择要聊什么
- 从具体的、容易回答的事实切入（在哪、几个人、什么时候），而不是抽象概念
- 不预设人生经历（不假设"幸福""成功""温馨"）

## 话题描述的要求
- **简短**：5-10个字
- **中性**：不预设情感色彩（不要用"温暖""快乐""难忘"等词）
- **具体**：有画面感，利用用户的年代背景让话题有时代感
- **信息导向**：话题应该能引出大量具体信息，而不只是情感回忆

好的例子：
- "小时候住的那个地方"（生活轨迹——能问出地址、房子、邻居、环境）
- "第一份工作"（工作经历——能问出单位、工种、同事、待遇）
- "上学那会儿"（求学经历——能问出学校、老师、同学、日常）
- "家里的人"（家庭——能问出每个家庭成员的信息）

不好的例子：
- "最像自己的一件事" ← 太抽象，问不出具体信息
- "一直留着的东西" ← 容易陷入物件细节
- "推了你一把的人" ← 预设了某种正面影响

## 开场白的要求
- 自然亲切，像晚辈和长辈聊天
- 以一个**具体的、关于事实的问题**结尾（在哪、几个、什么时候），不要问感受
- 问题必须中性，不预设答案
- 50-100字

## 对话背景信息（context）的要求
- 这个 context 会注入到采访者的 system prompt 中，指导采访者在这个话题下应该重点收集什么信息
- 必须包含：
  1. 用户的基础信息（年龄、家乡）
  2. **精选的时代记忆**：从上面的时代记忆中，挑选与该话题最相关的 3-5 条，用自然的叙述语言融入
  3. **信息收集重点**：在这个话题下，采访者应该重点搞清楚哪些信息（具体的人、事、地、时间）
  4. 这是首次对话，采访者应注意建立信任，节奏放慢
- 字数控制在 100-500 字

## 年龄范围（age_start, age_end）
- 根据话题对应的人生阶段估算年龄范围
- 参考：童年 0-12，上学 6-18，青年 18-30，中年 30-55，晚年 55+
- 如果话题不限于特定阶段，可以用较宽的范围

## 输出格式（JSON）
{{
    "options": [
        {{
            "topic": "话题描述",
            "greeting": "开场白",
            "context": "对话背景信息",
            "age_start": 0,
            "age_end": 12
        }}
    ]
}}

只输出 JSON，不要其他内容。
"""


def build(user_profile: str, era_memories: str = "", option_count: int = 4) -> str:
    """构建首次话题生成的 prompt"""
    if not era_memories or era_memories.strip() == "":
        era_memories = "（暂无时代记忆）"

    min_dimensions = max(3, option_count - 1)

    return PROMPT.format(
        user_profile=user_profile,
        era_memories=era_memories,
        option_count=option_count,
        min_dimensions=min_dimensions
    )
