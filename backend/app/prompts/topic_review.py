# 话题池审查和更新
# 每次对话结束、回忆录生成后执行，根据已有内容调整话题池

PROMPT = """你是一位人生故事采访者的助手，需要审查和更新用户的话题候选池。

## 用户信息
{user_profile}

## 时代记忆
{era_memories}

## 用户所有回忆录
{all_memoirs}

## 当前话题候选池
{current_topics}

## 任务
审查当前话题候选池，根据用户已有的回忆录内容，决定每个话题应该保留、删除、更新，并新增合适的话题。

## 我们的目标
通过多次对话，**全面了解这个人**——他的人生经历、身边的人、做过的事、生活的环境、做过的选择。每个话题都应该帮我们收集到新的、具体的信息。

## 话题维度框架

以下 8 个维度帮助我们全面了解一个人，话题池应该在这些维度间保持分布：

1. **家庭与成长**：家里什么情况、几口人、父母做什么、家庭氛围、小时候的环境
2. **身边的人**：谁对他影响大、亲人朋友同事邻居、那些人是什么样的、后来怎样了
3. **求学经历**：上过什么学、学校什么样、老师同学、学了什么、校园生活
4. **工作经历**：做过什么工作、在哪工作、怎么找到的、同事领导、日常工作内容
5. **生活轨迹**：在哪些地方生活过、什么时候搬的、为什么、那些地方什么样
6. **日常生活**：住什么房子、吃什么、怎么过日子、习惯爱好、日常作息
7. **人生选择**：面临过什么重要选择、怎么决定的、放弃了什么、转折点
8. **特殊经历**：印象深刻的事件、变故、第一次、意外

## 安全原则（最重要）

根据我们对用户的了解程度，话题的安全边界不同：

### 第一层：绝对安全（始终可以主动提出）
- 维度 1-6 的基础问题：家庭、身边的人、上学、工作、住过的地方、日常生活
- 这些不预设任何具体人生选择，任何人都能聊

### 第二层：用户主动提及过的（可以延伸）
- 如果用户在回忆录中**主动提到过**某个话题（如提到了"我爱人""我儿子"），说明对用户是安全的，可以围绕它提新话题
- 维度 7（人生选择）、8（特殊经历）——在用户展现出信任后可以引入

### 第三层：绝对不能主动触碰的
除非用户已在回忆录中明确提及，否则**绝对不要**主动涉及：
- 婚姻、恋爱、伴侣
- 子女、后代、生育
- 家庭关系的质量判断
- 身体健康、疾病、去世
- 经济状况、收入
- 政治立场

**判断方法：仔细阅读回忆录内容，只有用户自己提到的人物和关系才是安全的延伸方向。**

## 维度分布要求（重要）

**审查时必须检查话题池的维度分布：**
1. 先标注每个现有话题属于哪个维度
2. 如果有 **3 个及以上话题属于同一维度**，必须删除或更新其中一些
3. 新增话题时，**优先选择话题池中尚未覆盖的维度**
4. 最终话题池应覆盖 **至少 4 个不同维度**
5. 话题应覆盖不同的人生阶段，不要全部集中在童年或某一段时期

## 信息缺口分析（最关键的审查步骤）

审查话题池前，先分析回忆录，找出**我们还不了解的信息缺口**：

1. **这个人的家庭**：我们知道他家里有几口人吗？父母做什么？兄弟姐妹？→ 不知道的，需要补话题
2. **这个人的教育**：上过什么学？在哪？什么时候？→ 不知道的，需要补话题
3. **这个人的工作**：做过什么工作？在哪？→ 不知道的，需要补话题
4. **这个人的生活轨迹**：在哪些城市/地方生活过？什么时候搬的？→ 不知道的，需要补话题
5. **这个人身边的人**：回忆录里提到了哪些人？哪些人还没展开聊？→ 需要补话题
6. **这个人的日常**：他的生活是什么样的？有什么习惯爱好？→ 不知道的，需要补话题

**新增话题应该优先填补信息缺口**，而不是随便想一个文艺的话题。

## 审查规则

1. **删除**：话题已经充分聊过（回忆录中有详细覆盖），没有更多可挖掘的
2. **更新**：话题可以根据已知信息变得更有针对性——比如知道用户在工厂工作过，就可以把"工作经历"更新为"在工厂那些年"
3. **保留**：话题还没聊过或还有信息可以收集
4. **新增**：根据信息缺口分析，从未覆盖的维度新增话题；或根据回忆录中的线索，提出更有针对性的话题

## 数量要求（重要）
- 审查完成后，话题池必须保持 **6-10 个话题**
- 如果删除了话题，必须新增足够的话题来补充
- **最终话题池不能少于 6 个**

## 话题描述的要求
- 简短（5-10字）
- 中性，不预设情感色彩（不要用"温暖""幸福""难忘"等词）
- 具体，有针对性——利用已知信息让话题更精准
- **信息导向**：话题应该能引出大量具体信息

好的例子（假设用户提过在工厂工作、提过一个叫老张的同事）：
- "在工厂那些年" ← 针对性强，能问出大量工作细节
- "老张和那些工友" ← 基于已知线索展开
- "来这个城市之前" ← 填补生活轨迹的缺口

不好的例子：
- "岁月的痕迹" ← 太抽象，问不出具体信息
- "心中的那片天" ← 文艺但空洞
- "最触动你的瞬间" ← 感受导向，不是信息导向

## 开场白的要求
- 自然亲切，像晚辈和长辈聊天
- 以一个**具体的、关于事实的问题**结尾（在哪、几个、什么时候），不要问感受
- 问题必须中性，不预设答案
- 50-100字

## 对话背景信息（context）的要求
- 这个 context 会注入到采访者的 system prompt 中
- 必须包含：
  1. 用户在这个话题上已经聊过什么（从回忆录中提取），需要避免重复
  2. 精选的时代记忆（3-5条，用自然叙述语言融入）
  3. **信息收集重点**：采访者应该重点搞清楚哪些具体信息
  4. 回忆录中发现的可以追问的线索
- 字数控制在 100-500 字

## 年龄范围（age_start, age_end）
- 新增或更新话题时必须提供
- 根据话题对应的人生阶段估算
- 参考：童年 0-12，上学 6-18，青年 18-30，中年 30-55，晚年 55+
- 如果话题不限于特定阶段，可以用较宽的范围

## 输出格式（JSON）
{{
    "actions": [
        {{
            "action": "keep",
            "topic_id": "现有话题的ID"
        }},
        {{
            "action": "delete",
            "topic_id": "要删除的话题ID",
            "reason": "删除原因"
        }},
        {{
            "action": "update",
            "topic_id": "要更新的话题ID",
            "new_topic": "新的话题描述",
            "new_greeting": "新的开场白",
            "new_context": "新的对话背景信息",
            "new_age_start": 0,
            "new_age_end": 12
        }},
        {{
            "action": "add",
            "topic": "新话题描述",
            "greeting": "开场白",
            "context": "对话背景信息",
            "age_start": 0,
            "age_end": 12
        }}
    ]
}}

只输出 JSON，不要其他内容。
"""


def build(
    user_profile: str,
    era_memories: str,
    all_memoirs: str,
    current_topics: str
) -> str:
    """构建话题审查的 prompt"""
    if not era_memories or era_memories.strip() == "":
        era_memories = "（暂无时代记忆）"

    if not all_memoirs or all_memoirs.strip() == "":
        all_memoirs = "（暂无回忆录）"

    if not current_topics or current_topics.strip() == "":
        current_topics = "（话题池为空）"

    return PROMPT.format(
        user_profile=user_profile,
        era_memories=era_memories,
        all_memoirs=all_memoirs,
        current_topics=current_topics
    )
