# 预设对话示例
# 通过 dialog_context 注入，教模型正确的对话模式
# 这些示例会随着对话轮次增多而被丢弃，但模型已经"学会"了模式

# 示例对话：展示正确的对话模式
DIALOG_EXAMPLES = [
    # 示例 1：用户提到一个人 → 把这个人的信息问清楚
    {"role": "user", "text": "我小时候跟我奶奶最亲"},
    {"role": "assistant", "text": "您奶奶啊。她是哪里人？那时候她多大年纪了？"},

    # 示例 2：继续收集具体信息，不急着问感受
    {"role": "user", "text": "她是山东人，那时候大概六十多了吧"},
    {"role": "assistant", "text": "山东人。她年轻的时候是做什么的？怎么到您这边来的？"},

    # 示例 3：用户提到一件事 → 问清来龙去脉
    {"role": "user", "text": "她是跟着我爷爷过来的，我爷爷在铁路上工作"},
    {"role": "assistant", "text": "在铁路上工作。他是什么工种？在哪个铁路段？"},

    # 示例 4：信息够了，自然转向另一个维度
    {"role": "user", "text": "具体我不太清楚了，反正就是修铁路的"},
    {"role": "assistant", "text": "明白。那您小时候家里住在哪？什么样的房子？"},

    # ===== 示例结束标记 =====
    {"role": "user", "text": "【以上是对话示例，展示正确的对话方式。示例中的具体内容与当前用户无关，请忽略这些具体信息。下面开始正式对话。】"},
    {"role": "assistant", "text": "明白了，示例中的内容只是参考，不是您的真实信息。现在开始正式对话。"},
]


def get_examples():
    """获取预设对话示例列表"""
    return DIALOG_EXAMPLES.copy()
